/**
 * Multiple mode delivery method
 * 
 * @category   Innoexts
 * @package    Innoexts_Warehouse
 * @author     Innoexts Team <developers@innoexts.com>
 */
class Innoexts_Warehouse_Model_Adminhtml_System_Config_Source_Warehouse_Mode_Multiple_Delivery_Method 
{
    /**
     * Get warehouse helper
     * 
     * @return Innoexts_Warehouse_Helper_Data
     */
    protected function getWarehouseHelper()
    {
        return Mage::helper('warehouse');
    }
    /**
     * Options getter
     * 
     * @return array
     */
    public function toOptionArray()
    {
        $array = array();
        foreach ($this->getWarehouseHelper()->getConfig()->getMultipleModeDeliveryMethods() as $deliveryMethod) {
            array_push($array, array('value' => $deliveryMethod->getId(), 'label' => $deliveryMethod->getTitle()));
        }
        return $array;
    }
}