/**
 * Rss observer
 *
 * @category   Innoexts
 * @package    Innoexts_Warehouse
 * @author     Innoexts Team <developers@innoexts.com>
 */
class Innoexts_Warehouse_Model_Observer_Rss
{
    /**
     * Get warehouse helper
     *
     * @return Innoexts_Warehouse_Helper_Data
     */
    protected function getWarehouseHelper()
    {
        return Mage::helper('warehouse');
    }
    /**
     * convert order item to quote item (reserved for future purposes)
     * 
     * @param 	Varien_Event_Observer $observer
     * @return 	Innoexts_Warehouse_Model_Observer_Rss
     */
    public function prepareCatalogNotifyStockCollectionSelect(Varien_Event_Observer $observer)
    {
        $event = $observer->getEvent();
        $collection = $event->getCollection();
        if ($collection) {
            $collection->getSelect()->distinct(true);
        }
        return $this;
    }
}